SELECT
  cl.sifra,
  cl.naziv,
  COUNT(lps.id) AS broj_rata,
  MIN(lps.datum_dospijeca) AS od,
  MAX(lps.datum_dospijeca) AS do_,
  ROUND(SUM(lps.rata_total_eur)::numeric, 2) AS ukupno_za_platiti
FROM prod_company_loans cl
LEFT JOIN prod_loan_payment_schedule lps ON lps.loan_id = cl.id
GROUP BY cl.sifra, cl.naziv
ORDER BY cl.sifra;


