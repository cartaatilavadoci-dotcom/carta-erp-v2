-- PLAN OTPLATE KREDITA (prod_loan_payment_schedule)
-- Datum: 2026-04-23
-- ============================================================
-- Seed rate iz HBOR PDF planova otplate (Plan_otplate_scan/).
-- Samo buduće rate (od 31.05.2025 nadalje) — ono što je žuto markirano u PDF-u.
--
-- NP-17/14 i NP-18/14 HALA se odvajaju u 2 zapisa u prod_company_loans
-- (dosad spojeni u 1). Novi zapis NP-18/14 dobiva fiksan UUID.
-- ============================================================


-- ============================================================
-- 1. Tablica plana otplate
-- ============================================================
CREATE TABLE IF NOT EXISTS prod_loan_payment_schedule (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  loan_id UUID NOT NULL REFERENCES prod_company_loans(id) ON DELETE CASCADE,
  redni_br INTEGER,
  datum_dospijeca DATE NOT NULL,

  -- Kolone iz PDF-a plana otplate
  rata_total_eur NUMERIC(12,2),          -- KAMATE + OTPLATA
  kamate_eur NUMERIC(12,2),
  porez_na_kamate_eur NUMERIC(12,2) DEFAULT 0,
  otplatna_kvota_eur NUMERIC(12,2),      -- GLAVNICA
  ostatak_duga_eur NUMERIC(12,2),        -- OSTATAK DUGA nakon ove rate

  -- Status plaćanja
  placeno BOOLEAN DEFAULT false,
  placeno_datum DATE,
  placeno_iznos_eur NUMERIC(12,2),
  napomena TEXT,

  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_loan_schedule_loan ON prod_loan_payment_schedule(loan_id);
CREATE INDEX IF NOT EXISTS idx_loan_schedule_datum ON prod_loan_payment_schedule(datum_dospijeca);
CREATE INDEX IF NOT EXISTS idx_loan_schedule_unpaid ON prod_loan_payment_schedule(datum_dospijeca) WHERE placeno = false;
CREATE UNIQUE INDEX IF NOT EXISTS idx_loan_schedule_unique ON prod_loan_payment_schedule(loan_id, datum_dospijeca);

COMMENT ON TABLE prod_loan_payment_schedule IS
  'Plan otplate po kreditu — rate iz HBOR PDF-ova. Popunjava se ručno iz skenova.';

-- RLS
ALTER TABLE prod_loan_payment_schedule ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Allow all for authenticated" ON prod_loan_payment_schedule;
CREATE POLICY "Allow all for authenticated" ON prod_loan_payment_schedule
  FOR ALL USING (true) WITH CHECK (true);


